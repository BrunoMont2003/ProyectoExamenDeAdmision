    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (cboAula.getSelectedIndex() == 0 || cboCarrera.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento");
            } else {

                String nomCarrera = cboCarrera.getSelectedItem().toString();
                carrera = CarrerasDAO.getInstancia().buscarCarrerasPorNombre(nomCarrera);
                ep.mostraPostulantePorAC(cboAula.getSelectedItem().toString(), carrera.getCodigoCarrera(), modelo);
            }
        } catch (SQLException ex) {
            System.out.println("ERROR: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

