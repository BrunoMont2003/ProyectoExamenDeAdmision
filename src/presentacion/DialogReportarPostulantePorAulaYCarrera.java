    private void cboCarreraItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCarreraItemStateChanged
        try {
            cboAula.setEnabled(true);
            String nomCarrera = cboCarrera.getSelectedItem().toString();
            carrera = CarrerasDAO.getInstancia().buscarCarrerasPorNombre(nomCarrera);
            if (carrera != null) {
                Areas area = CarrerasDAO.getInstancia().buscarAreaDeUnaCarrera(carrera.getCodigoCarrera());
                if (area != null) {

                    this.cboAula.setModel(ep.ObtIdAulaPorArea(area.getIdArea()));
                }
            }
        } catch (SQLException ex) {
            System.out.println("ERROR: " + ex.getMessage());
        }
    }//GEN-LAST:event_cboCarreraItemStateChanged


