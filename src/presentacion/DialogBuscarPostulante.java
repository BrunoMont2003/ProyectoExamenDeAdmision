    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String idPostulante = cboIdPostulante.getSelectedItem().toString();
        try {
            Postulante postulante = postulantedao.buscarPostulante(idPostulante);
            txtNombres.setText(postulante.getNombres());
            txtApellidoPaterno.setText(postulante.getApellido_paterno());
            txtApellidoMaterno.setText(postulante.getApellido_materno());
            txtIdAula.setText(postulante.getAula().getIdAula());
            txtNumeroAula.setText(String.valueOf(postulante.getAula().getnAula()));
            txtIdCarrera.setText(postulante.getCarrera().getCodigoCarrera());
            txtNombreCarrera.setText(postulante.getCarrera().getNombreCarrera());
            txtIdModalidad.setText(postulante.getModalidad().getIdModalidad());
            txtNombreModalidad.setText(postulante.getModalidad().getNombreM());
            btnSeleccionar.setEnabled(true);

        } catch (SQLException ex) {
            System.out.println("ERROR: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed

        try {
            postulanteSelec.setIdPostulante(cboIdPostulante.getSelectedItem().toString());
            postulanteSelec.setNombres(txtNombres.getText());
            postulanteSelec.setApellido_paterno(txtApellidoPaterno.getText());
            postulanteSelec.setApellido_materno(txtApellidoMaterno.getText());
            Modalidad modalidad = new Modalidad(txtIdModalidad.getText(), txtNombreModalidad.getText());
            postulanteSelec.setModalidad(modalidad);
            
            Carreras carrera = CarrerasDAO.getInstancia().buscarCarreras(txtIdCarrera.getText());
            postulanteSelec.setCarrera(carrera);
            Aula aula = AulaDAO.getInstancia().buscarAula(txtIdAula.getText());
            postulanteSelec.setAula(aula);
            this.dispose();
        } catch (SQLException ex) {
            System.out.println("ERROR: "+ex.getMessage());
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

